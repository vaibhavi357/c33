function keyPressed(){
    //check the speed to avoid bird resetting back while the bird is still moving
    if(keyCode === 32 && bird.body.speed < 1){
        // empty the trajectory array when the space bar is pressed so that multiple trajectories are not created on the way back
        bird.trajectory = [];
        //reset the position of the bird near the slingshot to avoid the rapid swinging of the rubber band when attached from far off
        Matter.Body.setPosition(bird.body, {x: 200 , y: 50});
       slingshot.attach(bird.body);
    }
}

async function getBackgroundImg(){
    var response = await fetch("http://worldtimeapi.org/api/timezone/Asia/Kolkata");
    var responseJSON = await response.json();

    var datetime = responseJSON.datetime;
    var hour = datetime.slice(11,13);
    
    if(hour>=06 && hour<=19){
        bg = "sprites/bg1.png";
    }
    else{
        bg = "sprites/bg2.jpg";
    }

    backgroundImg = loadImage(bg);
    console.log(backgroundImg);
}

